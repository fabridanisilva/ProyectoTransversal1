    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        try{
        int id = Integer.parseInt( jtIdAlumno.getText());
        
        ArrayList<Materia> materias = inscripcionData.obtenerMateriasCursadas(id);
        DefaultComboBoxModel<Materia> comboBoxMateria = new DefaultComboBoxModel();
        
            for (Materia materia1 : materias) {
                comboBoxMateria.addElement(materia1);
            }
            jcMaterias.setModel(comboBoxMateria);
        
        
        }catch(NumberFormatException ex){
        
        JOptionPane.showMessageDialog(null, "Tiene que ingresar numeros!!");
        }
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

