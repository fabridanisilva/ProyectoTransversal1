    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
        int idAlumno = Integer.parseInt( jtIdAlumno.getText());
        int idMateria = Integer.parseInt(jtIdMateria.getText());
        int nota = Integer.parseInt(jtNota.getText());
        
        alumno = alumnoData.buscarAlumno(idAlumno);
        materia = materiaData.buscarMateria(idMateria);
        
        inscripcion = new Inscripcion(alumno,materia,nota);
        inscripcionData.guardarInscripcion(inscripcion);
        
        
        
        }catch(NumberFormatException ex){
        
            JOptionPane.showMessageDialog(null, "Tiene que ingresar numeros!!");
        
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

